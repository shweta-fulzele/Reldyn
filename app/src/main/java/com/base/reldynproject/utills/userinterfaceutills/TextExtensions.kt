// Create extension function for TextStyle

fun String.addDot(): String {
    return "$this."
}

fun String.addComma(): String {
    return "$this,"
}

fun String.addColon(): String {
    return "$this:"
}


fun String.addQuestionMark(): String {
    return "$this?"
}

fun String.addSpace(): String {
    return "$this "
}